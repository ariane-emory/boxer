use crate::block::*;

////////////////////////////////////////////////////////////////////////////////
pub struct RandomUsize {
  output: OutputSignalRef<usize>,
}
////////////////////////////////////////////////////////////////////////////////
impl RandomUsize {
  pub fn new() -> Self {
    let mut r = RandomUsize {
      output: new_signal(0),
    };

    r.step();
    r
  }
}
////////////////////////////////////////////////////////////////////////////////
impl Steppable for RandomUsize {
  fn step(&mut self) {
    self.output.set(rand::random());
  }
}
////////////////////////////////////////////////////////////////////////////////
impl HasOutputSignal<usize> for RandomUsize {
  fn output(&self) -> &OutputSignalRef<usize> {
    &self.output
  }
}
