mod errstring;
pub use errstring::*;

////////////////////////////////////////////////////////////////////////////////////////////////////
const NOISY: bool = true;

////////////////////////////////////////////////////////////////////////////////////////////////////
// Temporarily public, move this somewhere else!
pub fn noisy_println(args: std::fmt::Arguments) {
  if NOISY {
    println!("{}", args);
  }
}

////////////////////////////////////////////////////////////////////////////////////////////////////
#[macro_export]
macro_rules! noisy_println {
  ($($arg:tt)*) => {
    noisy_println(format_args!($($arg)*));
  }
}
